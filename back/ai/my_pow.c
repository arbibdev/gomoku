long	my_pow(long nb, int pow)
{
	int		c;
	long	factor;

	if (pow == 0)
		return (1);
	factor = nb;
	c = 1;
	while (c < pow)
	{
		nb = nb * factor;
		c += 1;
	}
	return (nb);
}
