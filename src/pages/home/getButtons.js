const printText = (data, images, text) => {
    var piece
    for (var y = 0; y < text.length; y++){
        for (var x = 0; x < text[y].length; x++){
            if (text[y][x]){
                piece = getRandomInt(2) ? images.blackPiece : images.whitePiece
                data.ctx.drawImage(
                    piece,
                    data.widthBoard * (x + 1) - piece.width / 2,
                    data.heightBoard * (y + 1) - piece.height / 2)
            }
        }
    }
}

const printBackground = (data, images, text) => {
    for (var y = 0; y < text.length + 1; y++){
        for (var x = 0; x < text[0].length + 1; x++){
            data.ctx.drawImage(images.gomokuBoard, data.widthBoard * x, data.heightBoard * y)
        }
    }
}

const getImage = (text, data, images) => {
    data.canvas.width = data.widthBoard * (text[0].length + 1)
    data.canvas.height = data.heightBoard * (text.length + 1)
    printBackground(data, images, text)
    printText(data, images, text)
    var res = new Image()
    res.src = data.canvas.toDataURL()
    return res
}

const getButtons = (images, setButtons) => {
    const data = {}
    data.widthBoard = images.gomokuBoard.width
    data.heightBoard = images.gomokuBoard.height
    data.canvas = document.createElement("CANVAS");
    data.ctx = data.canvas.getContext('2d')
    var res = []
    res.humanvsia = getImage(getHumanVsIa(), data, images)
    res.humanvshuman = getImage(getHumanVsHuman(), data, images)
    res.humanvsia.onload = () => {
        res.humanvshuman.onload = () => {
            setButtons(res)
        }
    }
}

export default getButtons