function getRandomInt(max) {
    return Math.floor(Math.random() * max);
}

const printText = (data, images, text) => {
    for (var y = 0; y < text.length; y++){
        for (var x = 0; x < text[y].length; x++){
            if (text[y][x])
                data.ctx.drawImage(
                    getRandomInt(2) ? images[data.BLACKPIECE] : images[data.WHITEPIECE],
                    data.widthBoard * x + data.widthBoard / 2,
                    data.heightBoard * y + data.heightBoard / 2)
        }
    }
}

const printBackground = (data, images, text) => {
    for (var y = 0; y < text.length + 1; y++){
        for (var x = 0; x < text[0].length + 1; x++){
            data.ctx.drawImage(images[data.GOMOKUBOARD], data.widthBoard * x, data.heightBoard * y)
        }
    }
}

const getImage = (text, data, images) => {
    data.canvas.width = data.widthBoard * (text[0].length + 1)
    data.canvas.height = data.heightBoard * (text.length + 1)
    printBackground(data, images, text)
    printText(data, images, text)
    var res = new Image()
    res.src = data.canvas.toDataURL()
    return res
}

const write = (images, setImages) => {
    const data = {}
    data.GOMOKUBOARD = 0
    data.BLACKPIECE = 1
    data.WHITEPIECE = 2
    data.widthBoard = images[data.GOMOKUBOARD].width
    data.heightBoard = images[data.GOMOKUBOARD].height
    data.canvas = document.createElement("CANVAS");
    data.ctx = data.canvas.getContext('2d')
    var res = []
    res[0] = getImage(getHumanVsIa(), data, images)
    res[1] = getImage(getHumanVsHuman(), data, images)
    res[0].onload = () => {
        res[1].onload = () => {
            setImages(res)
        }
    }
}

const getImages = (setImages) => {
    loadImages().then(images => {
        write(images, setImages)
    })
}

export default getImages