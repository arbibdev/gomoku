const imagesToLoad = [gomokuBoard, blackPiece, whitePiece]
var imagesLoaded = 0

const loadImages = () => {
    return new Promise((resolve, reject) => {        
        var images = []
        for (var c = 0; c < imagesToLoad.length; c++){
            images[c] = new Image()
            images[c].src = imagesToLoad[c]
            // eslint-disable-next-line
            images[c].onload = () => {
                imagesLoaded += 1
                if (imagesLoaded === imagesToLoad.length)
                    resolve(images)
            }
        }
    })
}

